package com.dwsj.vo;

public class ContactVo {
	String telename;
	String telephone;

	public String getTelename() {
		return telename;
	}

	public void setTelename(String telename) {
		this.telename = telename;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

}
